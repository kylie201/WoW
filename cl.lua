local function obfuscate(str, key)
    local result = ""
    for i = 1, #str do
        local char = string.byte(str, i)
        local keyChar = string.byte(key, (i % #key) + 1)
        result = result .. string.char(bit.bxor(char, keyChar))
    end
    return result
end

