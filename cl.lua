function SecureWhitelist.isWhitelisted(clientID)
    clientID = string.upper(clientID)
    clientID = string.gsub(clientID, "[-]", "")
 
    local obfuscatedInput = obfuscate(clientID, secretKey)

    return whitelistSet[obfuscatedInput] == true
end


